package OperatorsAndConditionalStatements;

//4th file
public class JavaOperators {
    public static void main(String[] args){

//In float if any of the operand is float value then the result will be a floating value.
        System.out.println(9.0/5);
        System.out.println((7>5)&&(false));
        System.out.println((7>5)||(false));


    }
}


