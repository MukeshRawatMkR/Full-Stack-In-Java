/*
1. Primitive Data Types: These are the most basic data types built into Java. There are 8 primitives:

byte → 8-bit, range: -128 to 127, default value is 0.
short → 16-bit, range: -32,768 to 32,767
int → 32-bit, range: -2,147,483,648 to 2,147,483,647, default value is 0.
long → 64-bit, range: -2^63 to 2^63-1, default value is 0. with suffix L.

float → 32-bit, single-precision floating point, default value is 0.0, with suffix f.
double → 64-bit, double-precision floating point, default value is 0.0

char → 16-bit Unicode character (range: '\u0000' (0) to '\uffff')
boolean → values: true or false, default value is false.

2. Non-Primitive (Reference) Data Types: These are not predefined in Java (except String) and are created by the programmer. They reference objects in memory.

String (special, though technically a class)
Arrays (e.g., int[], String[])
Classes (e.g., Student, Car)
Interfaces
Enums
Objects (instances of classes)

*/

